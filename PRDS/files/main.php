                     <h3 class="tech-body"><i>In South  African hospitals the is a huge<br>
                        struggle in administration. The Current systems are only able to register the admittance and the discharge date of the
                        patient. The is no cloud based system that tracks the medical history of the patient. All that admin is done in hard copies
                        and stored in a safe place, However should a disaster occur, all of the data would be lost.<br><br>
                        
                        This is where PRDS becoms a necessity<br>
                    Patient Record Database System(PRDS) is a universal database system that is useed to capture and store patient details
                and diagnoses data from the first hospital visit and ca be stored for a lifetime.
            
            This means that a returning patient record can be easily tracked whether from public,private or campus medical facility,
        making it easy for the next doctor to take over from the previous doctor.
